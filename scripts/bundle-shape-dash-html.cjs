/**
 * Inlines minigames/shapedash/www/*.html|css|js into a single TS export for WebView (iOS/Android/Web).
 * Run: node scripts/bundle-shape-dash-html.cjs
 */
'use strict';

const fs = require('fs');
const path = require('path');

const root = path.join(__dirname, '..');
const www = path.join(root, 'minigames', 'shapedash', 'www');
const outFile = path.join(root, 'minigames', 'shapedash', 'shapeDashInlineHtml.generated.ts');

const css = fs.readFileSync(path.join(www, 'styles.css'), 'utf8');
const js = fs.readFileSync(path.join(www, 'game.js'), 'utf8');

const html = `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0, maximum-scale=1.0, user-scalable=no">
  <title>Shape Dash</title>
  <style>${css.replace(/<\/style>/gi, '')}</style>
</head>
<body>
  <canvas id="c"></canvas>
  <script>${js.replace(/<\/script>/gi, '<\\/script>')}</script>
</body>
</html>`;

const header = `/**\n * AUTO-GENERATED by scripts/bundle-shape-dash-html.cjs — edit www/ and re-run bundle.\n */\n`;
const body = `${header}export const SHAPE_DASH_INLINE_HTML: string = ${JSON.stringify(html)};\n`;

fs.writeFileSync(outFile, body, 'utf8');
console.log('Wrote', outFile, `(${Math.round(body.length / 1024)} KB)`);
